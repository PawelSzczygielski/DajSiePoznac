            _amGymBoosterServiceClient = new AmGymBoosterServiceClient();
            _amGymBoosterServiceClient.GetAllTrainingsCompleted += (sender, args) =>
            {
                for (int index = 0; index < args.Result.Count; index++)
                {
                    var training = args.Result[index];
                    var viewModel = new ItemViewModel();
                    viewModel.LineOne = $"Training No.{index+1} started {training.StartTime.ToShortDateString()}";
                    viewModel.LineTwo = $"Exercises count: {training.Exercises.Count}";
                    viewModel.LineThree = $"{training.Exercises.First().Type.Name}{Environment.NewLine}" +
                                          $"{string.Join(Environment.NewLine, training.Exercises.SelectMany(s=>s.Series).Select(e=> "Serie " + index + ":" + e.NumberOfReps+ "*" + e.WeightInKilos + "kg"))}";
                    Items.Add(viewModel);
                }
            };
        }

